def add_op(a, b):
    return a + b
def sub_op(a, b):
    return a - b
def mul_op(a, b):
    return a * b
def div_op(a, b):
    return a / b

def max_op(a, b):
    if a > b:
        return a
    else:
        return b
def min_op(a, b):
    if a < b:
        return a
    else:
        return b
def reminder(a, b):
    return a % b